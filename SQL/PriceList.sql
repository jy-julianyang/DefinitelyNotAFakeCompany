DROP TABLE IF EXISTS SCATBOROUGH.dbo.PriceList;

CREATE TABLE SCATBOROUGH.dbo.PriceList (
_SupplierId	CHAR(2)	NOT NULL
,_PricelistId	CHAR(8) PRIMARY KEY
,_Name	VARCHAR(50) NOT NULL
,_NumberOfDoors	INT NOT NULL
,_NumberOfSeats INT NOT NULL
,_MilesPerHour INT NOT NULL
,_MaximumPayload INT NOT NULL
,_UnitCost	INT NOT NULL
,_UnitSale	INT NOT NULL
);

